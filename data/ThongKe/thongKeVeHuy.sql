WITH AllMonths AS (
    SELECT 1 AS MonthNumber
    UNION SELECT 2
    UNION SELECT 3
    UNION SELECT 4
    UNION SELECT 5
    UNION SELECT 6
    UNION SELECT 7
    UNION SELECT 8
    UNION SELECT 9
    UNION SELECT 10
    UNION SELECT 11
    UNION SELECT 12
)

SELECT 
    AM.MonthNumber,
    COALESCE(COUNT(LV.MaDatVe) - COUNT(LV.MaTKKH), 0) AS SLVeHuy
FROM 
    AllMonths AM
LEFT JOIN 
    LSDatVe LV ON YEAR(LV.NgayDatVe) = YEAR(GETDATE()) AND MONTH(LV.NgayDatVe) = AM.MonthNumber AND LV.MaTKKH IS NULL
GROUP BY 
    AM.MonthNumber
ORDER BY 
    AM.MonthNumber;